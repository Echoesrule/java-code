import  java.util.*;
public class TwoDArrayLists {
    public static void main(String[] args) {
        ArrayList<String> bakeryList=new ArrayList();
        bakeryList.add("Pasta");
        bakeryList.add("Doughnuts");
        bakeryList.add("Garlic bread");


        ArrayList<String> FruitsList=new ArrayList();
        FruitsList.add("Apples");
       FruitsList.add("Oranges");
       FruitsList.add("Watermelon");


        ArrayList<String> VegetablesList=new ArrayList();
        VegetablesList.add("Spinach");
        VegetablesList.add("Tomatoes");
        VegetablesList.add("Onions");

        System.out.println(bakeryList);
        System.out.println(FruitsList);
        System.out.println(VegetablesList);


    }
}
